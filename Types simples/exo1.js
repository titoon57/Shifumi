let premierNombre = parseInt(prompt("Entrez le premier nombre :"));
let deuxiemeNombre = parseInt(prompt("Entrez le deuxième nombre :"));

let addition = premierNombre + deuxiemeNombre;
let soustraction = premierNombre - deuxiemeNombre;
let multiplication = premierNombre * deuxiemeNombre;
let division = premierNombre / deuxiemeNombre;

console.log(`Le résultat de l'addition est : ${addition}`);
console.log(`Le résultat de la soustraction est : ${soustraction}`);
console.log(`Le résultat de la multiplication est : ${multiplication}`);
console.log(`Le résultat de la division est : ${division}`);